# 📧 Email System Report Script (Using `mail` Command)

This simple Bash script sends an email with a system report file as an attachment using the `mail` command.

---

## 📄 What the Script Does

- Sends an email to `sanjaysanjay55750@gmail.com`
- Subject: `SystemReport`
- Message body: `Your System Report Generated`
- Attachment: `/project9/file.txt`
- Prints a confirmation message after sending

